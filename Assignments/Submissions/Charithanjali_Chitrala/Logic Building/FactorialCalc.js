// DEFINE FUNCTION factorial(n)
//     SET result = 1
//     FOR i FROM 1 TO n DO:
//         result = result * i
//     ENDFOR
//     RETURN result
// END FUNCTION
// PRINT factorial(5)
// END





function factorial(n) {
    let result = 1;
    for (let i = 1; i <= n; i++) {
        result *= i;
    }
    return result;
}
console.log(factorial(5)); 

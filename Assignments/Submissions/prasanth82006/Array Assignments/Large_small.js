let student=[3,2,2,4,1,10,8,8,12,9]
let max=student[0];
for(let i=0;i<student.length;i++){
     if(student[i]>=max){
          max=student[i]
     }
}
let min=student[0];
for(let i=0;i<student.length;i++){
     if(student[i]<=min){
          min=student[i]
     }
}
console.log(`The Maximum Element is: ${max}`);
console.log(`The Minimum Element is: ${min}`);